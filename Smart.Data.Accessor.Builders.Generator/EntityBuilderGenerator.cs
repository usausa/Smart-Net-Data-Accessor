namespace Smart.Data.Accessor.Builders.Generator;

using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

using SourceGenerateHelper;

[Generator]
public sealed class EntityBuilderGenerator : IIncrementalGenerator
{
    private const string Ns = "Smart.Data.Accessor.Builders.";
    private const string NameAttributeName = "Smart.Data.Accessor.Attributes.NameAttribute";
    private const string IgnoreAttributeName = "Smart.Data.Accessor.Attributes.IgnoreAttribute";
    private const string AutoGeneratedAttributeName = "Smart.Data.Accessor.Attributes.AutoGeneratedAttribute";
    private const string KeyAttributeName = "Smart.Data.Accessor.Attributes.KeyAttribute";
    private const string BuilderContextFullName = "Smart.Data.Accessor.Builders.BuilderContext";

    private static readonly (string Attribute, BuilderKind Kind)[] Targets =
    [
        (Ns + "SelectBuilderAttribute", BuilderKind.Select),
        (Ns + "SelectSingleBuilderAttribute", BuilderKind.SelectSingle),
        (Ns + "UpdateBuilderAttribute", BuilderKind.Update),
        (Ns + "DeleteBuilderAttribute", BuilderKind.Delete),
        (Ns + "CountBuilderAttribute", BuilderKind.Count),
        (Ns + "TruncateBuilderAttribute", BuilderKind.Truncate),
    ];

    private enum BuilderKind
    {
        Select,
        SelectSingle,
        Update,
        Delete,
        Count,
        Truncate,
    }

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var providers = Targets.Select(t =>
        {
            var kind = t.Kind;
            return context.SyntaxProvider
                .ForAttributeWithMetadataName(
                    t.Attribute,
                    static (s, _) => s is MethodDeclarationSyntax,
                    (ctx, _) => (ctx, kind))
                .Collect();
        }).ToArray();

        var combined = providers[0];
        for (var i = 1; i < providers.Length; i++)
        {
            var local = providers[i];
            combined = combined.Combine(local).Select((pair, _) => pair.Left.AddRange(pair.Right));
        }

        context.RegisterSourceOutput(combined, static (spc, src) => Execute(spc, src));
    }

    private static void Execute(SourceProductionContext context, ImmutableArray<(GeneratorAttributeSyntaxContext Ctx, BuilderKind Kind)> entries)
    {
        var groups = new Dictionary<INamedTypeSymbol, List<(IMethodSymbol Method, AttributeData Attr, MethodDeclarationSyntax Syntax, BuilderKind Kind)>>(SymbolEqualityComparer.Default);

        foreach (var (ctx, kind) in entries)
        {
            if (ctx.TargetSymbol is not IMethodSymbol method || ctx.TargetNode is not MethodDeclarationSyntax syntax)
            {
                continue;
            }
            if (!method.IsPartialDefinition)
            {
                context.ReportDiagnostic(Diagnostic.Create(Diagnostics.InvalidMethod, syntax.Identifier.GetLocation(), method.Name));
                continue;
            }

            var container = method.ContainingType;
            if (syntax.Parent is not ClassDeclarationSyntax containerSyntax || !containerSyntax.Modifiers.Any(t => t.Text == "partial"))
            {
                context.ReportDiagnostic(Diagnostic.Create(Diagnostics.InvalidContainer, syntax.Identifier.GetLocation(), container.Name));
                continue;
            }

            var attrName = Targets.First(t => t.Kind == kind).Attribute;
            var attr = method.GetAttributes().FirstOrDefault(a => a.AttributeClass?.ToDisplayString() == attrName);
            if (attr is null)
            {
                continue;
            }

            if (method.Parameters.Length < 1 ||
                method.Parameters[0].Type.ToDisplayString() != BuilderContextFullName ||
                method.Parameters[0].RefKind != RefKind.Ref)
            {
                context.ReportDiagnostic(Diagnostic.Create(Diagnostics.InvalidSignature, syntax.Identifier.GetLocation(), method.Name));
                continue;
            }

            if (kind == BuilderKind.Update && method.Parameters.Length < 2)
            {
                context.ReportDiagnostic(Diagnostic.Create(Diagnostics.BuilderRequiresEntityParameter, syntax.Identifier.GetLocation(), method.Name));
                continue;
            }

            if (!groups.TryGetValue(container, out var list))
            {
                list = [];
                groups[container] = list;
            }
            list.Add((method, attr, syntax, kind));
        }

        foreach (var kv in groups)
        {
            var source = Emit(context, kv.Key, kv.Value);
            var ns = kv.Key.ContainingNamespace.IsGlobalNamespace
                ? "global"
                : kv.Key.ContainingNamespace.ToDisplayString().Replace('.', '_');
            var filename = $"{ns}_{kv.Key.Name}.EntityBuilders.g.cs";
            context.AddSource(filename, SourceText.From(source, Encoding.UTF8));
        }
    }

    private static string Emit(SourceProductionContext context, INamedTypeSymbol container, List<(IMethodSymbol Method, AttributeData Attr, MethodDeclarationSyntax Syntax, BuilderKind Kind)> entries)
    {
        var builder = new SourceBuilder();
        builder.AutoGenerated();
        builder.EnableNullable();
        builder.Indent().Append("#pragma warning disable").NewLine();
        builder.NewLine();

        var ns = container.ContainingNamespace.IsGlobalNamespace ? string.Empty : container.ContainingNamespace.ToDisplayString();
        if (!string.IsNullOrEmpty(ns))
        {
            builder.Namespace(ns);
            builder.NewLine();
        }

        var typeMaps = MappingResolver.ReadTypeMaps(container);

        builder.Indent().Append(AccessibilityText(container.DeclaredAccessibility)).Append(" partial class ").Append(container.Name).NewLine();
        builder.BeginScope();

        var first = true;
        foreach (var (method, attr, syntax, kind) in entries)
        {
            if (!first)
            {
                builder.NewLine();
            }
            first = false;
            EmitMethod(context, builder, container, method, attr, syntax, kind, typeMaps);
        }

        builder.EndScope();
        return builder.ToString();
    }

    private static void EmitMethod(
        SourceProductionContext context,
        SourceBuilder builder,
        INamedTypeSymbol container,
        IMethodSymbol method,
        AttributeData attr,
        MethodDeclarationSyntax syntax,
        BuilderKind kind,
        Dictionary<ITypeSymbol, TypeMapInfo> typeMaps)
    {
        var entityType = (INamedTypeSymbol)attr.ConstructorArguments[0].Value!;
        var tableName = entityType.Name;
        foreach (var kv in attr.NamedArguments)
        {
            if (kv.Key == "Table" && kv.Value.Value is string s)
            {
                tableName = s;
            }
        }

        var props = ReadEntityColumns(entityType);

        var ctxParamName = method.Parameters[0].Name;
        var methodAccess = AccessibilityText(method.DeclaredAccessibility);
        var staticPrefix = method.IsStatic ? "static " : string.Empty;

        var paramText = new StringBuilder();
        paramText.Append("ref global::Smart.Data.Accessor.Builders.BuilderContext ");
        paramText.Append(ctxParamName);
        for (var i = 1; i < method.Parameters.Length; i++)
        {
            var p = method.Parameters[i];
            paramText.Append(", ");
            paramText.Append(p.Type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat));
            paramText.Append(' ');
            paramText.Append(p.Name);
        }

        builder.Indent().Append(methodAccess).Append(" ").Append(staticPrefix).Append("partial void ")
            .Append(method.Name).Append("(").Append(paramText.ToString()).Append(")").NewLine();
        builder.BeginScope();

        builder.Indent().Append("var cmd = ").Append(ctxParamName).Append(".Command;").NewLine();

        switch (kind)
        {
            case BuilderKind.Update:
                EmitUpdate(context, builder, container, method, syntax, entityType, tableName, props, typeMaps);
                break;
            case BuilderKind.Delete:
                EmitDelete(context, builder, method, syntax, entityType, tableName, props, typeMaps);
                break;
            case BuilderKind.Count:
                EmitCount(builder, tableName);
                break;
            case BuilderKind.Truncate:
                EmitTruncate(builder, tableName);
                break;
            case BuilderKind.Select:
                EmitSelect(builder, tableName, props);
                break;
            case BuilderKind.SelectSingle:
                EmitSelectSingle(context, builder, method, syntax, entityType, tableName, props, typeMaps);
                break;
        }

        builder.EndScope();
    }

    private static void EmitUpdate(
        SourceProductionContext context,
        SourceBuilder builder,
        INamedTypeSymbol container,
        IMethodSymbol method,
        MethodDeclarationSyntax syntax,
        INamedTypeSymbol entityType,
        string tableName,
        List<EntityColumn> props,
        Dictionary<ITypeSymbol, TypeMapInfo> typeMaps)
    {
        var entityParamName = method.Parameters[1].Name;
        var settable = props.Where(p => !p.IsKey && !p.IsAutoGenerated).ToList();
        var keys = props.Where(p => p.IsKey).ToList();

        if (keys.Count == 0)
        {
            context.ReportDiagnostic(Diagnostic.Create(
                Diagnostics.NoKeyForBuilder,
                syntax.Identifier.GetLocation(),
                entityType.Name,
                method.Name));
        }

        var sqlBuilder = new StringBuilder();
        sqlBuilder.Append("UPDATE ").Append(Quote(tableName)).Append(" SET ");
        for (var i = 0; i < settable.Count; i++)
        {
            if (i > 0)
            {
                sqlBuilder.Append(", ");
            }
            sqlBuilder.Append(Quote(settable[i].Column)).Append(" = ").Append(Marker).Append(settable[i].PropertyName);
        }
        if (keys.Count > 0)
        {
            sqlBuilder.Append(" WHERE ");
            for (var i = 0; i < keys.Count; i++)
            {
                if (i > 0)
                {
                    sqlBuilder.Append(" AND ");
                }
                sqlBuilder.Append(Quote(keys[i].Column)).Append(" = ").Append(Marker).Append("k_").Append(keys[i].PropertyName);
            }
        }

        builder.Indent().Append("cmd.CommandText = ").Append(EncodeStringLiteral(sqlBuilder.ToString())).Append(";").NewLine();

        var reportLocation = syntax.Identifier.GetLocation();
        foreach (var p in settable)
        {
            var pName = Marker + p.PropertyName;
            ParameterEmitter.EmitEntityPropertyParameter(
                builder,
                pName,
                $"{entityParamName}.{p.PropertyName}",
                p.Symbol,
                typeMaps,
                container,
                context,
                reportLocation);
        }
        foreach (var p in keys)
        {
            var pName = Marker + "k_" + p.PropertyName;
            ParameterEmitter.EmitEntityPropertyParameter(
                builder,
                pName,
                $"{entityParamName}.{p.PropertyName}",
                p.Symbol,
                typeMaps,
                container,
                context,
                reportLocation);
        }
    }

    private static void EmitDelete(
        SourceProductionContext context,
        SourceBuilder builder,
        IMethodSymbol method,
        MethodDeclarationSyntax syntax,
        INamedTypeSymbol entityType,
        string tableName,
        List<EntityColumn> props,
        Dictionary<ITypeSymbol, TypeMapInfo> typeMaps)
    {
        var keys = props.Where(p => p.IsKey).ToList();
        if (keys.Count == 0)
        {
            context.ReportDiagnostic(Diagnostic.Create(
                Diagnostics.NoKeyForBuilder,
                syntax.Identifier.GetLocation(),
                entityType.Name,
                method.Name));
        }

        var sqlBuilder = new StringBuilder();
        sqlBuilder.Append("DELETE FROM ").Append(Quote(tableName));
        var ki = 0;
        var extraParams = method.Parameters.Length - 1;
        if (keys.Count > 0 && extraParams > 0)
        {
            sqlBuilder.Append(" WHERE ");
            for (var i = 1; i < method.Parameters.Length && ki < keys.Count; i++, ki++)
            {
                if (ki > 0)
                {
                    sqlBuilder.Append(" AND ");
                }
                var paramName = method.Parameters[i].Name;
                sqlBuilder.Append(Quote(keys[ki].Column)).Append(" = ").Append(Marker).Append(paramName);
            }
        }

        builder.Indent().Append("cmd.CommandText = ").Append(EncodeStringLiteral(sqlBuilder.ToString())).Append(";").NewLine();

        ki = 0;
        for (var i = 1; i < method.Parameters.Length && ki < keys.Count; i++, ki++)
        {
            var paramName = method.Parameters[i].Name;
            var markerName = Marker + paramName;
            ParameterEmitter.EmitMethodParameterBinding(
                builder,
                markerName,
                paramName,
                method.Parameters[i].Type,
                typeMaps);
        }
    }

    private static void EmitCount(SourceBuilder builder, string tableName)
    {
        var sql = "SELECT COUNT(*) FROM " + Quote(tableName);
        builder.Indent().Append("cmd.CommandText = ").Append(EncodeStringLiteral(sql)).Append(";").NewLine();
    }

    private static void EmitTruncate(SourceBuilder builder, string tableName)
    {
        // Always emit `TRUNCATE TABLE`. SQLite has no TRUNCATE keyword — SQLite users
        // should use raw SQL ([Execute] + .sql with DELETE FROM) instead of [TruncateBuilder].
        var sql = "TRUNCATE TABLE " + Quote(tableName);
        builder.Indent().Append("cmd.CommandText = ").Append(EncodeStringLiteral(sql)).Append(";").NewLine();
    }

    private static void EmitSelect(SourceBuilder builder, string tableName, List<EntityColumn> props)
    {
        // Sprint 5.3 will refine column projection. Sprint 5.2 emits the full column list (excluding [Ignore])
        // so Example.ConsoleApplication.SelectAll remains operational across the phase boundary.
        var cols = string.Join(", ", props.Select(p => Quote(p.Column)));
        var sql = "SELECT " + cols + " FROM " + Quote(tableName);
        builder.Indent().Append("cmd.CommandText = ").Append(EncodeStringLiteral(sql)).Append(";").NewLine();
    }

    private static void EmitSelectSingle(
        SourceProductionContext context,
        SourceBuilder builder,
        IMethodSymbol method,
        MethodDeclarationSyntax syntax,
        INamedTypeSymbol entityType,
        string tableName,
        List<EntityColumn> props,
        Dictionary<ITypeSymbol, TypeMapInfo> typeMaps)
    {
        var keys = props.Where(p => p.IsKey).ToList();
        if (keys.Count == 0)
        {
            context.ReportDiagnostic(Diagnostic.Create(
                Diagnostics.NoKeyForBuilder,
                syntax.Identifier.GetLocation(),
                entityType.Name,
                method.Name));
        }

        var sqlBuilder = new StringBuilder();
        sqlBuilder.Append("SELECT ");
        for (var i = 0; i < props.Count; i++)
        {
            if (i > 0)
            {
                sqlBuilder.Append(", ");
            }
            sqlBuilder.Append(Quote(props[i].Column));
        }
        sqlBuilder.Append(" FROM ").Append(Quote(tableName));

        var ki = 0;
        if (keys.Count > 0 && method.Parameters.Length > 1)
        {
            sqlBuilder.Append(" WHERE ");
            for (var i = 1; i < method.Parameters.Length && ki < keys.Count; i++, ki++)
            {
                if (ki > 0)
                {
                    sqlBuilder.Append(" AND ");
                }
                sqlBuilder.Append(Quote(keys[ki].Column)).Append(" = ").Append(Marker).Append(method.Parameters[i].Name);
            }
        }

        builder.Indent().Append("cmd.CommandText = ").Append(EncodeStringLiteral(sqlBuilder.ToString())).Append(";").NewLine();

        ki = 0;
        for (var i = 1; i < method.Parameters.Length && ki < keys.Count; i++, ki++)
        {
            var paramName = method.Parameters[i].Name;
            var markerName = Marker + paramName;
            ParameterEmitter.EmitMethodParameterBinding(
                builder,
                markerName,
                paramName,
                method.Parameters[i].Type,
                typeMaps);
        }
    }

    private static List<EntityColumn> ReadEntityColumns(INamedTypeSymbol entityType)
    {
        var list = new List<EntityColumn>();
        foreach (var p in entityType.GetMembers().OfType<IPropertySymbol>())
        {
            if (p.DeclaredAccessibility != Accessibility.Public || p.IsStatic || p.GetMethod is null)
            {
                continue;
            }
            if (p.GetAttributes().Any(a => a.AttributeClass?.ToDisplayString() == IgnoreAttributeName))
            {
                continue;
            }
            var column = p.GetAttributes()
                .FirstOrDefault(a => a.AttributeClass?.ToDisplayString() == NameAttributeName)
                ?.ConstructorArguments.FirstOrDefault().Value as string ?? p.Name;
            var isKey = p.GetAttributes().Any(a => a.AttributeClass?.ToDisplayString() == KeyAttributeName);
            var isAuto = p.GetAttributes().Any(a => a.AttributeClass?.ToDisplayString() == AutoGeneratedAttributeName);
            list.Add(new EntityColumn(column, p.Name, isKey, isAuto, p));
        }
        return list;
    }

    private const char Marker = '@';

    private static string Quote(string name) => "\"" + name + "\"";

    private static string EncodeStringLiteral(string value)
    {
        var sb = new StringBuilder(value.Length + 2);
        sb.Append('"');
        foreach (var ch in value)
        {
            switch (ch)
            {
                case '\\': sb.Append("\\\\"); break;
                case '"': sb.Append("\\\""); break;
                case '\r': sb.Append("\\r"); break;
                case '\n': sb.Append("\\n"); break;
                case '\t': sb.Append("\\t"); break;
                default: sb.Append(ch); break;
            }
        }
        sb.Append('"');
        return sb.ToString();
    }

    private static string AccessibilityText(Accessibility a) => a switch
    {
        Accessibility.Public => "public",
        Accessibility.Internal => "internal",
        Accessibility.Private => "private",
        Accessibility.Protected => "protected",
        Accessibility.ProtectedOrInternal => "protected internal",
        Accessibility.ProtectedAndInternal => "private protected",
        _ => "internal",
    };

    private readonly record struct EntityColumn(string Column, string PropertyName, bool IsKey, bool IsAutoGenerated, IPropertySymbol Symbol);
}
